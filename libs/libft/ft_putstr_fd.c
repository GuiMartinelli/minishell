#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	if (!s)
		write (1, "(null)", 6);
	else
	{
		while (*s && *s != 3)
		{
			write(fd, &*s, 1);
			s++;
		}
	}
}
